    private void cargarIngredientes() throws SQLException, ClassNotFoundException {

        IngredienteVo ingrediente = new IngredienteVo();
        String nombre = "";
        int precio = 0;

        PreparedStatement ps = null;
        ResultSet rs = null;

        Connection c = Conexion.obtener();
        ps = c.prepareStatement("SELECT * FROM ingrediente");
        rs = ps.executeQuery();
        ResultSetMetaData rsmd = rs.getMetaData();

        short cantidadColumnas = (short) rsmd.getColumnCount();

        while (rs.next()) {

            for (short i = 0; i < cantidadColumnas; i++) {

                if (i % 2 == 0) {

                    nombre = (String) rs.getObject(i + 1);
                    ingrediente.setNombre(nombre);

                } else {

                    precio = (int) rs.getObject(i + 1);
                    ingrediente.setPrecio((short) precio);

                    this.ingredientes.add(ingrediente);
                    ingrediente = new IngredienteVo();

                }

            }

        }

    }

